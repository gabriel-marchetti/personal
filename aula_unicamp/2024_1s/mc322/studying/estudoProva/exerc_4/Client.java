public class Client{
    private String name;
    private Integer id;
    private String birthdate;
    private Integer age;
    private String phoneNumber;

    public Client( String name,
                   Integer id,
                   String birthdate,
                   Integer age,
                   String phoneNumber 
    ){
        this.name = name;
        this.id = id;
        this.birthdate = birthdate;
        this.age = age;
        this.phoneNumber = phoneNumber;
    }

}